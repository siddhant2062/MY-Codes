package pattern;
import java.util.Scanner;
public class triangular_number_pattern {
	public static void main(String args[]) {
		Scanner s=new Scanner(System.in);
		
		int i=1;
		int n=s.nextInt();
		while(i<=n) {
			int j=1;
			while(j<=i) {
				System.out.print(i);
				j=j+1;
			}
			System.out.println();
			i=i+1;
			}
		}
	}


